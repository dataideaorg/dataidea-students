# Capitalize

text = "i love teaching"

capitalized_text = text.capitalize()
print(capitalized_text)

# upper case
upped_text = text.upper()
print(upped_text)

# Replace parts of string
replaced_text = text.replace('teaching','coding')
print(replaced_text)

# check if text is in a string
value = "love"
result = value in text
print(result)