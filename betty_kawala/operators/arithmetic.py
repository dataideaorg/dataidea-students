# Arithmetic operators perform mathematical operations on numbers.

# Operator      Description
# +             Addition
# -             Subtraction
# /             Division
# *             Multiplication
# %             Modulus (remainder)
# **            Exponentiation (power)
# //            Floor division (rounds down)

# Examples
number1 = 5
number2 = 3

# Addition
result = number1 + number2

# Subtraction
result = number1 - number2

# Division
result = number1 / number2

# Multiplication
result = number1 * number2

# Modulus (remainder)
result = number1 % number2

# Exponentiation (power)
result = number1 ** number2

# Floor division (rounds down)
result = number1 // number2






