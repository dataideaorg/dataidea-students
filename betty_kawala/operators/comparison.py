# Comparison operators compare two values and return a boolean, 
# either True or False.

# List of comparison operators:

# Name                  Operator
# Equal                 ==
# Not equal             !=
# Greater than          >
# Less than             <
# Greater than or equal >=
# Less than or equal    <=

# Example
number1 = 10
number2 = 20

print(number1 >= number2)