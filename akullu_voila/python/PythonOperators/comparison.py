# A comparison operator compares its operands and returns
# a Boolean value based on whether the comparison is 
# True of False

# Table of Contents
# Name              Operation
# Equality          ==
# Inequality        !=
# Greater than      >
# Less than         <
# Greater or equal  >=
# Less or equal     <=

# Examples
# Equality 
check_voila = ('Voila' == 'Viola')

# Inequality 
check_voila = ('Voila' != 'Viola')

# Greater or Equal
greater_or_equal = 34 >= 43

# Tip
print('Voila' == 'Viola' == 'Voila')
#       False == True => False
