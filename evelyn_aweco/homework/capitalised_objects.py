# how to captilise objects in python

#.upper()
# this function is used to convert a string into capital/uppercase letters.

#.lower()
# this one is used to convert a string into small letters/ lowercase letters

#.capitalize()
# this function is used to capitalise the first character of a string and converts the rest into lower case.

#.title()
# this function is used to capitalize  every first character of a word in a string  and converts the
# rest of the characters in the string to small letters.

# Examples below 
greetings = "hullo world"
capitalized_greetings = greetings.capitalize()
print(capitalized_greetings)

hey = "Hello world"
uppercase_hey = hey.upper()
print(uppercase_hey)

Hola = "HALLO WORLD"
lowercase_Hola = Hola.lower()
print(lowercase_Hola)

sentence = "hullo world"
titlecased_sentence = sentence.title()
print(titlecased_sentence)
