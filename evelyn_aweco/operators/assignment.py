# Assignment operators are used to 
# assign values to variables in python

# Name                      Operator
# assignment                   x = y
# addition assignment        x += y => x = x + y  
# subtraction assignment   x -= y => x = x - y 
# multiplication assng    x *= y => x = x * y
# division assng           x /= y 
# exponentiation assng      x  **= y
# remainder assign          x %= y
# floor division assgn      x //= y

# Example
x = 7
y = 3

x += y
x -= y

print(x)
