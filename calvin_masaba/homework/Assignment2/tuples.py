new_tuple = tuple(('watermellon',)) #A tuple of one element

print(original_tuple) #prints the original tuple elements

print(new_tuple) #prints the new tuple element

print(new_tuple + (original_tuple)) #adds new elements to a tuple and its achieved by creating a new tuple

